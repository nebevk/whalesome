/** Species with a clean pre-exploitation figure, for the depletion-and-recovery view. */
export const recoveryPairs = populations
  .filter(p => p.prewhalingApprox && p.currentLow > 0)
  .map(p => ({
    ...p,
    currentMid: Math.round((p.currentLow + p.currentHigh) / 2),
    pctRemaining: Math.round(((p.currentLow + p.currentHigh) / 2 / (p.prewhalingApprox as number)) * 100),
  }))
  .sort((a, b) => a.pctRemaining - b.pctRemaining)

